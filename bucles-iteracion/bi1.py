import os

#DECODIFICAR MENSAJE ENCRIPTADO
#input
msg=os.sys.argv[1].upper()

#bucle
for letra in msg:
    if letra=="V":
        print("Gracias")
    if letra =="W":
        print("por la compra")
    if letra=="X":
        print("regrese")
    if letra=="Y":
        print("pronto")

print("\n")
print("fin del bucle")
