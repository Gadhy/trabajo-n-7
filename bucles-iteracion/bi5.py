import os

#DECODIFICAR MENSAJE ENCRIPTADO
#input

msg=os.sys.argv[1].upper()

#bucle
for letra in msg:
    if letra=="A":
        print("Hola")
    if letra =="B":
        print("Mi amor")
    if letra=="C":
        print("Te quiero")
    if letra=="D":
        print("Mucho")
#fin literador
print("\n")
print("fin del bucle")
